package examples;

public class ex18 {

	public static void main(String[] args) {
//		연산자
		
//		자바 연산자
//		이항 연산자 : 산비논대
//		                                  비트단위 연산자
//		단항 : ++ -- ! (논리반전) (타입)     ~           
//		산술 : + - * / %                    << >> >>>   
//		논리 : && ||
//		삼항 : ? :
//		대입 : = += -= *= /= %=
		
//		단항 연산자 ++ -- !
		int i = 10;
		i++; 
		System.out.println( i );
		
		i--;
		System.out.println( i );
		
		System.out.println( !true );
		System.out.println( !false );

		
	}

}
