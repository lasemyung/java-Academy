package examples;

public class ex46 {

	public static void main(String[] args) {
		
		//while문
				//for문 패턴
				//for( 초기화; 조건; 증감) {
				//  수행문;  
				//}
				for(int i=0; i<10; i++) {
					System.out.println( i );
				}
				//while문 패턴
				//초기화;
				//while( 조건문 ) {
				//   수행문;
				//   증감문;
				//}
				int i=0;
				while( i<10 ) {
					System.out.println( i );
					i++;
				}

	}

}
